    public FormRelToco atualizarTabela() {
    DefaultTableModel model = (DefaultTableModel) tblToco.getModel();
    model.setRowCount(0);
    for (Toco t : BDToco.getInstance().listarTodos()) {
        model.addRow(new Object[]{
            t.getPlaca(), t.getMarca(), t.getAno(),
            t.getTipoCarroceria(), t.getAutonomiaKm()
        });
    }
    return this;
}
