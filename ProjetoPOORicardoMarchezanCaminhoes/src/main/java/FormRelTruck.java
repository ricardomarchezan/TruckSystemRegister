    public FormRelTruck atualizarTabela() {
    DefaultTableModel model = (DefaultTableModel) tblTruck.getModel();
    model.setRowCount(0);
    for (Truck t : BDTruck.getInstance().listarTodos()) {
        model.addRow(new Object[]{
            t.getPlaca(),
            t.getMarca(),
            t.getAno(),
            t.getQtdEixosTraseiros(),
            t.getTipoSuspensao()
        });
    }
    return this;
}

